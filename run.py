# श्री राम नाग जी, यह फाइल शिव एआई (Shiv AI) को शुरू करने के लिए है।
import uvicorn
import os

# सर्वर चलाने का मुख्य हिस्सा
if __name__ == "__main__":
    # हम 'uvicorn' का उपयोग कर रहे हैं जो आपके प्रोजेक्ट को 
    # टर्बो हाई स्पीड (Turbo High Speed) पर चलाने में मदद करेगा।
    
    print("शिव एआई (Shiv AI) शुरू हो रहा है...")
    
    uvicorn.run(
        "app.main:app", 
        host="0.0.0.0", 
        port=8000, 
        reload=True  # इससे कोड बदलते ही सर्वर अपने आप अपडेट हो जाएगा
    )

